package Matematica;

public class PORCENTAGEM {

	public static void main(String[] args) {
		
		int a, b, c;
		
		a = 30;	
		b = 400;	
		
		c = a * b / 100;
		
		System.out.println("30% de 400 é: " + c);
		
	//------------------------------------------------------------------//
		
		int d, e, f;
		
		d = 20;
		e = 360;
		
		f = d * e / 100;
		
		System.out.println("20% de 360 é: " + f);

	//------------------------------------------------------------------//
		
		double g, h, i;
		
		g = 65;
		h = 90;
		
		i = g * h / 100;
		
		System.out.println("65% de 90 é: " + i);
	}

}
